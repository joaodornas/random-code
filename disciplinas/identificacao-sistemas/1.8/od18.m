function dy = od18(t,y)

% a = 1;
% b = 1;
% c = 1;
% d = 1;
% e = 1;
% f = 1;
% g = 1;
% h = 1;

a = 0.311;
b = 0.518;
c = 1.036;
d = 0.311;
e = 0.161;
f = 4.599;
g = 2.469;
h = 0.322;


dy = zeros(3,1);

dy(1) = y(1) - y(1)^2 - (y(1)*y(2))/(y(1)+a);
dy(2) = -b*y(2) + (c*y(1)*y(2))/(y(1)+d) - (y(2)*y(3))/(y(2)+e);
dy(3) = f*y(3)^2 - (g*y(3)^2)/(y(2)+h);



end